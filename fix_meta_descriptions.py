META_DESC_RE = re.compile(r'<meta name="description" content="([^"]*)"')

def main():
    count = 0
    for filepath in sorted(glob.glob(f"{ARTICLES_DIR}/*.html")):
        slug = os.path.basename(filepath).replace('.html', '')
        if slug not in DESCRIPTIONS:
            continue
        with open(filepath) as f:
            html = f.read()
        new_desc = DESCRIPTIONS[slug]
        new_html = META_DESC_RE.sub(lambda m, nd=new_desc: f'<meta name="description" content="{nd}">', html, count=1)
        if new_html != html:
            with open(filepath, 'w') as f:
                f.write(new_html)
            count += 1
    print(f"Updated {count} articles with expanded meta descriptions")

if __name__ == "__main__":
    main()
