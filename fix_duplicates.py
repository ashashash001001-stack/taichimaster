    scored = []
    for o in others:
        score = 10 if o['category'] == article['category'] else 0
        score += o['viewWeight'] / 100
        scored.append((score, o))
    scored.sort(key=lambda x: -x[0])
    return [x[1] for x in scored[:n]]

